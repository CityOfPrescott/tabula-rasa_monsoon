function db_connect ($db_info)
{
	// Make connection to MySQL server
	if (!$dbc = @mysql_connect ($db_info['host'], $db_info['db_user'], $db_info['db_pass']))
	{
		// If it couldn't connect to MySQL
		// Print a message to the user and kill the script
		echo '<p class="error">kenny MySQL Error #'.mysql_errno().': '.mysql_error().'.</p>';
		exit();
	}
	// Select the database
	if (!mysql_select_db ($db_info['db_name']))
	{
		// Handle the error
		// Print a message to the user and kill the script
		echo '<p class="error">scott MySQL Error #'.mysql_errno().': '.mysql_error().'.</p>';
		exit();
	}

	return $dbc;
} // End db_connect()

